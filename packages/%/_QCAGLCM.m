;%QCAGLCM^INT^1^59547,73875^0
ZGETCLE(GL,CLE,NBCLE) 
 
 N I,J,AUX
 S J=1
 S AUX=$P(GL,"(",2,50)
 S AUX=$E(AUX,1,$L(AUX)-1)
CLESUIV 
 S I=J
 S:$P(AUX,",",I)="" NBCLE=I-1
 Q:$P(AUX,",",I)=""
TESTCLE 
 S CLE(I)=$P(AUX,",",I,J)
 S J=J+1
 G:(CLE(I)+0)=CLE(I) CLESUIV
 S L=$L(CLE(I))
 S TE=($E(CLE(I),1)="""")&($E(CLE(I),L)="""") I TE X "S CLE(I)="_CLE(I)
 G:TE CLESUIV
 G TESTCLE
 
FIN Q

