;%QCASTA^INT^1^59547,73875^0
ZSTACK ;
 
 
 
 
 
INITFI(GL) K @(GL) S @GL=0 Q
PUSH(GL,A) 
 S @GL@(@GL)=A,@GL=@GL+1
 
 Q
POP(GL) 
 I @GL=0 W !,"PILE VIDE N = ZERO ",@(GL) Q ""
 S @GL=@GL-1
 N %V S %V=@GL@(@GL) Q %V
PILEVIDE(GL) Q @GL=0
INDICE(GL) N %V S %V=@GL Q %V
 
INITLI 
 K @GL@
 S MIN=0,MAX=10
 S TOP=MIN,BOT=MIN
 Q
IN(GL,A) 
 I ((TOP+1)#MAX)=BOT W !,"LIFO PLEINE" Q
 S @GL@(TOP)=A,TOP=(TOP+1)#MAX
 Q
OUT(GL) 
 I TOP=BOT W !,"LIFO VIDE" Q "ERREUR"
 N
 S BOT=(BOT+1)#MAX
 N %V S %V=@GL@(B) Q %V

