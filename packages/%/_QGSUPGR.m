;%QGSUPGR^INT^1^59547,73876^0
GRSUPGRO ;
 S LIS=$Y G:$D(^PAD($J,PAD,LIS))=0 END1 S GRO=^PAD($J,PAD,LIS) G:ZOOM=0 SG1 D:GROZOOM=GRO ZOFF^%QGZOOM
SG1 S STR="OK pour detruire @"_GRO_"# O/N N=> " D ^%QGOPTIO R *X G:X'=79 FIN1 S OBJ=-1
LSG S OBJ=$N(@GROUPE@(1,GRO,"OBJET",OBJ)) G:OBJ=-1 FSG
 S @GROAFF@(OBJ)=@GROAFF@(OBJ)-@GROAFF@(OBJ,GRO)
 K @GROAFF@(OBJ,GRO)
 G LSG
FSG K @GROUPE@(1,GRO) I $D(GROSEL),GRO=GROSEL K GROSEL
 D CREPAD^%QGPAG G:AFFLIST FSG1 S AFFLIST=1 D ^%QGNONAF
FSG1 D APG^%QGPAG,APD^%QGPAG
FIN1 S RETOUR=1 Q
END1 D ^%VSQUEAK S RETOUR=2 Q

