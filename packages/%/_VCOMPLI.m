;%VCOMPLI^INT^1^59547,74034^0
%VCOMPLI(S) 
 N SC,GR,%U,CH,Y,X,L,N
 K ^LIGNE($I) S SC=S_",COMMENT" F GR=S,SC D TR
 S CPT=^%SCRE(S) F CH=1:1:CPT D:'($D(^%SCRE(S,CH,"ESPACE"))) MAJ
FIN Q
MAJ S Y=^%SCRE(S,CH,3),X=^%SCRE(S,CH,2),N=$O(^LIGNE($I,Y,X)) Q:N=""
 S ^%SCRE(S,CH,"ESPACE")=(X-1)_","_(N-1) Q
TR D:($D(^%SCRE(GR))#10)'=1 CPT S CPT=^%SCRE(GR) F CH=1:1:CPT S X=^%SCRE(GR,CH,2),Y=^%SCRE(GR,CH,3),L=^%SCRE(GR,CH,1) I (GR'[",")!(L["~") S ^LIGNE($I,Y,X)=CH
 Q
CPT F %U=1:1 Q:'($D(^%SCRE(GR,%U)))
 S ^%SCRE(GR)=%U-1 Q

