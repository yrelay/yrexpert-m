;%VMOV^INT^1^59547,74035^0
VMOV(XG,XD,YH,YB,X,Y) 
 
 
 
 
 
 
 
 
 
 
 
 D CURON^%VVIDEO
 S DX=XG,DY=YH X XY
LEC D ZFLECHE^%VZATOU
 G AUTRE:%F="",@%F
H G AV:$Y=YH S DY=$Y-1 X XY G LEC
B G AV:$Y=YB S DY=$Y+1 X XY G LEC
G G AV:$X=XG S DX=$X-1 X XY G LEC
D G AV:$X=XD S DX=$X+1 X XY G LEC
AUTRE S X=$X,Y=$Y Q X1
AV D ^%VSQUEAK G LEC
TEST N CH D POCLEPA^%VVIDEO W "XG,XD,YH,YB : " R CH Q:CH=""
 S CAR=$$VMOV($P(CH,",",1),$P(CH,",",2),$P(CH,",",3),$P(CH,",",4),.XX,.YY)
 D POCLEPA^%VVIDEO W "X=",XX,"  Y=",YY,"  CAR=",CAR R *REP G TEST:CAR'=1
 Q

