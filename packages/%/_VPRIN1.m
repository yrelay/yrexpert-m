;%VPRIN1^INT^1^59547,74035^0
VVPRIN1 ;
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
WINT(LI,CH,X) 
 N P1,P2,LCH,LLI,%I
 
 I X<0 Q LI
 S LCH=$L(CH),LLI=$L(LI)
 
 F %I=LLI:1:X-1 S LI=LI_" "
 S P1=$E(LI,1,X),P2=$E(LI,X+LCH,999)
 Q P1_CH_P2
TEST 
 N LI,CH,X,LIG
 W !,"Ligne : " R LI Q:LI="F"
 W !,"Chaine : " R CH
 W !,"Position : " R X
 S LIG=$$WINT(LI,CH,X)
 W !,"Ligne resultat = ",LIG
 G TEST
 
 
ADD(OO,TXT) 
 S @OO@($ZP(@OO@(""))+1)=TXT
 Q
 
SAUT(OO,N) 
 N I,A
 S A=$ZP(@OO@(""))
 F I=A+1:1:A+N S @OO@(I)=" "
 Q

