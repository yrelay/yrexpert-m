;JALTEST^INT^1^59547,74867^0
JALTEST ;
 
UN(ARC,DATE) 
 D JAV^JALACT2(ARC,1,DATE)
 Q
LISTE(LISTE) 
 S DATE=$P($H,",",1),ADR=$$^%QSCALVA("L0",LISTE,"ADRESSE"),NOMBRE=0
 S ^ARNAUD("DEBUT")=$H
 S ARC="" F U=0:0 S ARC=$O(@ADR@(ARC)) Q:NOMBRE=1000  Q:ARC=""  D GO S NOMBRE=NOMBRE+1
 S ^ARNAUD("FIN")=$H,^ARNAUD("NB")=NOMBRE
 Q
GO 
 D UN(ARC,DATE) S DATE=DATE+1 W ARC,$J("",20-$L(ARC))
 D PA^%QSGESTI($$ART^JALRPJ,ARC,"JALONNE.LE","1109",1)
 Q

