;TO3ALLRL^INT^1^59547,74869^0
TO3ALLRL ;
 
 S NUP1=1,PAR=-1 F UU=1:1 S PAR=$N(^[QUI]RANG(ARTI,PAR)) Q:PAR=-1  S ^PILR1($I,ARTI,NUP1)=PAR,^PILR2($I,ARTI,PAR)=1,NUP1=NUP1+1
 S NUP=-1 F UU=0:0 S NUP=$N(^PILR1($I,ARTI,NUP)) Q:NUP=-1  S PAR=^PILR1($I,ARTI,NUP) D TRAIT
FIN K NUP1,PAR,UU,NUP,FAT Q
TRAIT S FAT=-1 F VV=0:0 S FAT=$N(^ENTREE($I,ARTI,PAR,FAT)) Q:FAT=-1  I '($D(^PILR2($I,ARTI,FAT))) S ^PILR1($I,ARTI,NUP1)=FAT,^PILR2($I,ARTI,FAT)=1,NUP1=NUP1+1,^[QUI]RANG(ARTI,FAT)=1
END Q

