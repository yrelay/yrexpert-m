;TO3SAVOP^INT^1^59547,74870^0
TO3SAVOP ;
 
 K ^OTREETUD($I),^OLDB2($I),^%ANOM2($I) S ^OLTREF2($I)=%TREFC,^OLDEB2($I)=%1DEB,^OLDTREE($I)=NOMTREE,^OLDADR($I)=ADR,^PR2($I)=%PR,^ONVIRG($I)=NVIRG,^%ANOM2($I)=NOM,YF=-1
 F TT=0:0 S YF=$N(B(YF)) Q:YF=-1  S ^OLDB2($I,YF)=B(YF)
 S YF=-1 F TT=0:0 S YF=$N(NOM(YF)) Q:YF=-1  S ^%ANOM2($I,YF)=NOM(YF)
 S YF=-1 F TT=0:0 S YF=$N(TREETUD(YF)) Q:YF=-1  S ^OTREETUD($I,YF)=TREETUD(YF)
FIN K B,NOM,TREETUD Q

