;TO41AJSH^INT^1^59547,74870^0
TO41AJSH ;
 S %AJCMS=$$^%QZCHW("Ajout")
LOOP S CMS=0,SCR="ARTSTAND",NOMTAB="^[QUI]ARTSTAND",%DRES=2
 D CLEPAG^%VVIDEO,POCLEPA^%VVIDEO W $$^%QZCHW("Saisie de : ") S DX=$X D ^%VLEC G:(CTRLA=1)!((CTRLF=1)!(Y1="")) FIN G:'($D(^[QUI]EXPLICI(Y1))) LOOP S (%EXPERT,YA)=Y1 D ^TO4GEXPL,^TO4ACTIS G FS:ABENDSCR=1
 S REF=0 D ^TO41STOS
FS S DX=0,DY=23 X XY U 0
 W $$^%QZCHW("Fin de saisie : F       ATTENTION au No ordre !") R *F S F=$C(F) G:F'=$$^%QZCHW("F") LOOP2
 Q
LOOP2 
 D ^TO4MODIS G:ABENDSCR=1 FS D ^TO4STOSH G FS
FIN Q

