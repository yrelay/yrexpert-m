TO4GEXPL ;
 
 K ^TO4GEXPL($I),^ITO4GEXP($I),^JTO4GEXP($I) S TEXP="^[QUI]EXPLICI(YA)",NUEX1=1,NUEX2=-1
 F WWR=0:0 S NUEX2=$N(@TEXP@(NUEX2)) Q:NUEX2=-1  S ^TO4GEXPL($I,NUEX1)=$N(@TEXP@(NUEX1,-1)),^ITO4GEXP($I,$N(@TEXP@(NUEX2,-1)))=NUEX1,NUEX1=NUEX1+1
 S %NPAR=NUEX1-1,NUCC=1,PAR=-1 F ZZ=0:0 S PAR=$N(^ITO4GEXP($I,PAR)) Q:PAR=-1  S ^JTO4GEXP($I,NUCC)=PAR,NUCC=NUCC+1
FIN K NUCC,NUEX1,NUEX2,WWR,TEXP Q

