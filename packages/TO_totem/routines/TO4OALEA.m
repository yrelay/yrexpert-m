;TO4OALEA^INT^1^59547,74871^0
TO4OALEA ;
 
 D CLEPAG^%VVIDEO S DX=0,DY=5 X XY W $$^%QZCHW("Combien voulez-vous d'objets-cibles ? ") S DX=$X D ^%VLEC Q:(Y1="")!((CTRLA=1)!(CTRLF=1))  S PCH=Y1 D TO^%QZNBN1 G:'(ISNUM) TO4OALEA S NBOBJ=PCH\1
 K ^[QUI]TOTEXPER("GENCOMP") D NBPAR
 F UU=1:1:NBOBJ D TRAIT
 Q
TRAIT S OBJ="OBJET"_UU F VV=1:1:NBPAR S VPAR=$R(^TO4ALEA("NBLEG"))+1,OBJ=OBJ_"/P"_VV_":"_VPAR
 S ^[QUI]TOTEXPER("GENCOMP",$P(OBJ,"/",1))=OBJ
 Q
NBPAR S (NBPAR,NBP)=-1 F UU=0:0 S NBP=$N(^[QUI]EXPLICI("GENCOMP",NBP)) Q:NBP=-1  S NBPAR=NBP
 Q

