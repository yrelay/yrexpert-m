;TO4TRICI^INT^1^59547,74871^0
TO4TRICI ;
 
 K ^NUNEWPT($J),^NEWPT($J) D ^TO4TRICR,^TO4TRINO,^TO4TRICX
 I CAMAX=1 S ^NEWPT($J,PTMAX)=1 G GCYCL
LOOP I PTMAX'="" S PS=-1 F UU=0:0 S PS=$N(@%TS@(PTMAX,PS)) Q:PS=-1  D TRAIT
 D ^TO4TRICX G:CAMAX>1 LOOP
GCYCL K ^NUNEWPT($J) D ^TO4TRIMA
FIN K CAMAX,PS,PTMAX,NUM Q
TRAIT S NUM=$S($D(^NUNEWPT($J,PTMAX)):^NUNEWPT($J,PTMAX)+1,1:1),^NUNEWPT($J,PTMAX)=NUM
 K @%TS@(PTMAX,PS) S @%TC@(PTMAX)=@%TC@(PTMAX)-1
 S:$D(@%TS@(PS)) @%TS@(PTMAX_"/"_NUM,PS)=1,@%TC@(PTMAX_"/"_NUM)=1,^NEWPT($J,PTMAX_"/"_NUM)=1
 Q

