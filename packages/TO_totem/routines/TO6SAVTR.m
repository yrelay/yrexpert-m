TO6SAVTR ;
 
 K SAV6 S SAV6(0,"%TREFC")=%TREFC,SAV6(0,"NOMTREE")=NOMTREE,SAV6(0,"%PR")=%PR,SAV6(0,"STOP")=STOP,SAV6("YF")=-1
 F SAV6("TT")=0:0 S SAV6("YF")=$N(B(SAV6("YF"))) Q:SAV6("YF")=-1  S SAV6("B",SAV6("YF"))=B(SAV6("YF"))
FIN K SAV6("TT"),SAV6("YF"),B Q
RES S SAV6("YF")=-1 F SAV6("TT")=1:1 S SAV6("YF")=$N(SAV6(0,SAV6("YF"))) Q:SAV6("YF")=-1  S @SAV6("YF")=SAV6(0,SAV6("YF"))
 S SAV6("YF")=-1 F SAV6("TT")=1:1 S SAV6("YF")=$N(SAV6("B",SAV6("YF"))) Q:SAV6("YF")=-1  S B(SAV6("YF"))=SAV6("B",SAV6("YF"))
 K SAV6 Q

