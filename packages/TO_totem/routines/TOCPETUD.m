;TOCPETUD^INT^1^59547,74871^0
TOCPETUD(NUF,GLO) 
 ;; |-----------------------------------------------------------------|
 ;; | En entree NUF numero d'etude, GLO table contenant les resultats |
 ;; |        @GLO@(1,COMPOSANT,POSITION)                              |
 ;; |        @GLO@(2,POSITION,COMPOSANT)                              |
 ;; |  La position est la valeur du noeud dans l'arbre d'evaluation   |
 ;; |-----------------------------------------------------------------|
 K @(GLO) S NOMTREE="^[QUI]TREEWORK("""_NUF_"""",%TAD="TR^TOCPETUD" D ^%QCAGTW
FIN K NOMTREE,%TAD,CP,TW Q
TR S TW=$P($P(%T,",",2,999),")",1),CP=$P(@%T,"^",2)
 I (CP'="")&(TW'="") S @GLO@(1,CP,TW)=1,@GLO@(2,TW,CP)=1
 Q

