;TOGCOMBI^INT^1^59547,74872^0
TOGCOMBI(VC,SEP,CB) 
 
 K @(CB) S NU=1,PC=1,VPC="",VIM=""
 F %U=0:1 S VIM=$O(@VC@(VIM)) Q:VIM=""
 S VPM=%U
LP D:PC=VPM RFC S VPC=$O(@VC@(PC,VPC)) G:VPC="" VPCV S VIC(PC)=VPC I PC<VPM S PC=PC+1,VPC="" G LP
 S @CB@(NU)=VIM_VPC,NU=NU+1 G LP
VPCV G:PC=1 FIN S PC=PC-1,VPC=VIC(PC) G LP
RFC S VIM="" F %U=1:1:VPM-1 S VIM=VIM_VIC(%U)_SEP
 Q  S VIM=$E(VIM,1,$L(VIM)-1) Q
FIN K NU,PC,VPC,VIM,%U,VIC Q

