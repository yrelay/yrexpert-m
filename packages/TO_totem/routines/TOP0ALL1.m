;TOP0ALL1^INT^1^59547,74873^0
TOP0ALLC ;
 
 K ^POSENR($I),^POSFUL($I) S %NU=8,PAGE=1,NUMCLI=-1
 F NC=1:1 S NUMCLI=$N(^[QUI]PSRLFILI(NUMCLI)) Q:(NUMCLI="z")!(NUMCLI=-1)  D TRAIT
FIN Q
TRAIT S ARC=$N(^[QUI]PSRLFILI(NUMCLI,-1)),CLIENT=$N(^[QUI]PSRLFILI(NUMCLI,ARC,-1)),PROD=$N(^[QUI]PSRLFILI(NUMCLI,ARC,CLIENT,-1)),FICH=^[QUI]PSRLFILI(NUMCLI,ARC,CLIENT,PROD)
 S ^POSENR($I,PAGE,%NU)=NUMCLI_"^"_ARC_"^"_$P(FICH,"^",1)_"^"_$P(FICH,"^",6) I %NU=8 S ^POSFUL($I,PAGE)=NUMCLI
 S %NU=%NU+1 S:%NU=22 %NU=8,PAGE=PAGE+1 Q

