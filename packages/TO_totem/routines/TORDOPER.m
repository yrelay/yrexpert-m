;TORDOPER^INT^1^59547,74874^0
TORDOPER ;
 
 S REFUSE=0 I RESUL(IC)'="" S LEX=$L(RESUL(IC)),DX=$L(^%SCRE(SCR,IC,1))+^%SCRE(SCR,IC,2),DY=^%SCRE(SCR,IC,3) X XY F RS=1:1:LEX W " "
 G:(RESUL(IC-2)="")!(RESUL(IC-1)="") ECRI
 S XPAR=RESUL(IC-2),PHORD=RESUL(IC-1),ORD=-1 F RR=0:0 S ORDP=ORD,ORD=$N(^[QUI]PHASE(XPAR,PHORD,ORD)) G:ORD=-1 AFFECT
AFFECT I ORDP'=-1 G PLUS
 I RESUL(IC)="" S REFUSE=1 G FIN
 G ECRI
PLUS S RESUL(IC)=ORDP+1
ECRI S DX=$L(^%SCRE(SCR,IC,1))+^%SCRE(SCR,IC,2),DY=^%SCRE(SCR,IC,3) X XY W RESUL(IC)
FIN Q
ORDPH I %AJM="A" D ORDPH^%LXUC5
 Q

