 O 3 U 3 W $T(+0),"  ",$S," memoire ",! C 3
 D CLEPAG^%VVIDEO W "Simulation de saisie du S-F de l'article et de ses composants : " S DX=10,DY=DY+3 X XY D ^%VLEC G:(CTRLA=1)!((CTRLF=1)!(Y1="")) FIN2 S (YART,ARTI)=Y1 W " OK !"
