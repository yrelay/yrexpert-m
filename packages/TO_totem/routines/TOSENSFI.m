;TOSENSFI^INT^1^59547,74875^0
TOSENSFI ;
 
 F TB="BES","DEF" S DOM=-1 F %U=0:0 S DOM=$N(@TB@(DOM)) Q:DOM=-1  S ATT=-1 F %U=0:0 S ATT=$N(@TB@(DOM,ATT)) Q:ATT=-1  D T0
FIN K TB,BES,DEF,%U,ARAT,ATT,ATC Q
T0 S ARAT=$S(ATT["'":$P(ATT,"'",2),1:ARTI),ATC=$P(ATT,"'",1),^[QUI]SENSFICH(ARTI,0,%DOM,%NUFICH,TB,DOM,ARAT,ATC)=1 S:TB="DEF" ^[QUI]SENSFICH(ARTI,1,DOM,ATC,%NUFICH)=1
 Q:(%DOM'="NOMENC")&(%DOM'="IMPLICI")  Q:WB2'["~"  Q:DOM'="IMPLICI"
 I ATC'["~",ARAT'["~",$D(^[QUI]FULL(ARAT,ATC)) S NU=$O(^[QUI]INDEFIND(ARTI,%NUFICH,""))-1,^[QUI]INDEFIND(ARTI,%NUFICH,NU)="^V($I,"""_ARAT_""","""_ATC_""")"
 Q

