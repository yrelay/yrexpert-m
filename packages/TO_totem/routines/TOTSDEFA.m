;TOTSDEFA^INT^1^59547,74876^0
TOTSDEFA(DM) 
 
 S TSDEF=1,TDEF="^[QUI]PARINDEF"_$P(RBIS,",",1)_","""_DM_""","_$P(RBIS,",",2,999),PND=-1
 F UU=0:0 S PND=$N(@TDEF@(PND)) G:PND=-1 IND D TEST I TSDEF=0 G FIN
IND S RF=$P(SOURCE,",",2,999) G:'($D(^[QUI]REPFICH(YA,DM,RF))) FIN S FIC=^[QUI]REPFICH(YA,DM,RF)
 S TDEF="^[QUI]INDEFIND("""_YA_""","_FIC_")",ID="" F UU=0:0 S ID=$O(@TDEF@(ID)) G:ID="" FIN S PND=@TDEF@(ID) D TEST I TSDEF=0 G FIN
FIN K ID,TDEF,PND Q
TEST 
 I '($D(@PND)) S TSDEF=0 Q
 I @PND="1??" S TSDEF=0 Q
 Q

