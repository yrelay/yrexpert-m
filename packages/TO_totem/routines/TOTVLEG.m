;TOTVLEG^INT^1^59547,74876^0
TOTVLEG ;
 
 S VOK=0,IC=4,TSVIDE=1 G:VPA="" END I $D(^[QUI]EXPGIMP(PARA,VPA)) S VOK=1 Q
LOPLEG 
 S VLE=$P(CHP,"^",IC) G:VLE="" POPLEG S TSVIDE=0
 I (VLE["[")!(VLE["]") S VAL=VPA,BRAQUETTE=VLE D VERBRAC^TOTVERIB S VOK=ACC G POPLEG:VOK=0,VLEGOUT:VOK=1
 I VPA=VLE S VOK=1 G VLEGOUT
POPLEG S IC=IC+1 G:IC'>17 LOPLEG
VLEGOUT S:(VOK=0)&TSVIDE VOK=1 G:VOK>0 END
 D ^%VSQUEAK,POCLEPA^%VVIDEO W $$^%QZCHW("Valeur refusee, sauf si vous insistez : ") H 4 S ATT=(1-BATCH)*100,DX=$X,Y1="" D:ATT'=0 ^%VLEC K ATT S VPA1=Y1 D POCLEPA^%VVIDEO G:VPA1'=VPA END S VOK=1
END K IC,TSVIDE,VLE,VAL,BRAQUETTE,VPA1 Q

