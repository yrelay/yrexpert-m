;TOWCMRES^INT^1^59547,74876^0
TOWCMRES ;
 
 K RESUL,DEJALU,CL S K=1,CHP=1
 F %1UU=2:2 S Y=$P(%NOMT,"""",%1UU) Q:Y=""  S CL(%1UU\2)=Y
 I $D(@%NOMO)=1 S RS=@%NOMO D ^%VKMULC G S
 S %NOM=%NOMO D ^%VKMULC
S F %1UU=1:1 Q:'($D(^%SCRE(SCR,%1UU)))  D TRAIT
 K %RS,CL,RS,MSG,TIT,K,CHP,%1UU,Y,LIG
FIN Q
TRAIT I $D(^%SCRE(SCR,%1UU))=11 S RESUL(%1UU)=CL(K+1),K=K+1 G END
 S RESUL(%1UU)=$S($D(%RS(CHP)):%RS(CHP),1:"") K %RS(CHP) S CHP=CHP+1
END Q

