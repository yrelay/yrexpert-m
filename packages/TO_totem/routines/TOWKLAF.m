;TOWKLAF^INT^1^59547,74876^0
TOWKLAF ;
 K ^[QUI]ANTETUDE("LAF"),^[QUI]REFANTET("LAF") S LF=-1 F U=0:0 S LF=$N(^[QUI]GAMLAF(LF)) Q:LF=-1  I LF'="z" K ^[QUI]GAMLAF(LF)
 S TB=$P($T(KILL),";;",2) F U=1:1 S GL=$P(TB,",",U) Q:GL=""  S GM="GALz",GL="^[QUI]"_GL F V=0:0 S GM=$N(@GL@(GM)) Q:GM=-1  Q:$E(GM,1,3)'="GAM"  D K1
 Q
K1 S TA=$P($T(KILL),";;",2) F W=1:1 S TK=$P(TA,",",W) Q:TK=""  K @("^[QUI]"_TK_"("""_GM_""")") W "."
 Q
KILL ;;SECT,CONS,PHAS,DUREE,COUT,RAISON,DIM,TVPAR,AUTOPLAF

