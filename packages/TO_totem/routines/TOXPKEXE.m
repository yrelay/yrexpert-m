;TOXPKEXE^INT^1^59547,74877^0
TOXPKEXE ;
 S %AJCMS=""
 S NB=0 K ^NETOIERR($I)
LOOP S CMS=0,SCR="NETOIERR"
 D CLEPAG^%VVIDEO,B^%VACTISC G FS:ABENDSCR=1
 D POCLEPA^%VVIDEO W $$^%QZCHW("Confirmez-vous cette suppression (O/N) ? ") R *CONF G:CONF'=79 LOOP D POCLEPA^%VVIDEO
 S NB=NB+1,ANET="" F C=1:1:5 S ANET=ANET_RESUL(C)_"^"
 S ^NETOIERR($I,NB)=ANET
FS S DX=0,DY=23 X XY U 0
 W $$^%QZCHW("Fin de saisie : F") R *F S F=$C(F) G:F'="F" LOOP S NBANET=NB I $D(^NETOIERR($I)) D ^TOXKEXEC
FIN K ^NETOIERR($I),NBANET,ANET,NB,CONF,CMS,%AJCMS,SCR,F Q

