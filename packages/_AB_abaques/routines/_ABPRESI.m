;%ABPRESI^INT^1^59547,73864^0
%ABPRESI ;
 
 S MSGT=^TABIDENT(WHOIS,"ENTETE"),MSG="" F UU=1:1 S CC=$E(MSGT,UU) Q:CC=""  S MSG=MSG_CC_" "
 S LMSG=$L(MSG)
 S MSG="" U IMPR W #,!,!,!,!,!,!,!,!,!,!,?40-(LMSG\2),MSG
 S LMSG1=$L(MSG1)
 W !,!,?40-(LMSG1\2),MSG1,!,!
 S MSG="" F UU=1:1 S CC=$E(WHOIS,UU) Q:CC=""  S MSG=MSG_CC_" "
 W ?40-($L(MSG)\2),MSG,!,! D ^%ABTOTEP
 W !,!,!
 S LMSG2=$L(MSG2)
 W !,!,?40-(LMSG2\2),MSG2,!,!
 S MSG=$$^%QZCHW("  date  ")_%DAT,LMSG=$L(MSG)
 W !,?40-(LMSG\2),MSG
 S MSG="Copyright Yrelay",LMSG=$L(MSG)
 W !,!,?40-(LMSG\2),MSG
FIN W ! H 3 Q

