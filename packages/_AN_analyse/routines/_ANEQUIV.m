;%ANEQUIV^INT^1^59547,73866^0
ANSEQUIV(A1,C1,AFF1,P1) 
 
 
 
 
 N ATT,BAS,CTX,REF,VAL,VAR
 S REF=$$RED^%ANA(A1,1)
 S VAL=$$SUBST^%ANARBR2(REF,.C1,AFF1,.P1) Q:'(P1) ""
 S ATT=$$RED^%ANA($$RED^%ANA(REF,1),"TEXTE")
 S VAR=$$RED^%ANA($$RED^%ANA(REF,3),"TEXTE")
 S BAS=$$NOMLOG^%QSF(C1(VAR,"BASE")) Q:BAS="" VAL
 S CTX=$$SUBST^%ANARBR2($$RED^%ANA(A1,2),.C1,AFF1,.P1) Q:'(P1) ""
 Q:'($D(^[QUI]RQQ(BAS,ATT,VAL,CTX))) VAL
 Q $P(^[QUI]RQQ(BAS,ATT,VAL,CTX),"^",1)

