;%ATNUT2^INT^1^59547,73866^0
%ATNUT2 ;;09:21 AM  16 Jan 1993; 16 Jan 93  9:12 AM ; 16 Jan 93 10:03 AM
 
FINMENU(%MN,NOMATN) 
 N NUMUC,NUN,%NUN,GLO
 S GLO="^[QUI]RQSGLO(""ATN"")"
 S NUMUC=$ZP(@%MN@(""))
 S NUN="" F %NUN=0:0 S NUN=$O(@GLO@(NOMATN,"UCONT",NUN)) Q:NUN=""  S NUMUC=NUMUC+1,@%MN@(NUMUC)=@GLO@(NOMATN,"UCONT",NUN)_"^AUTRE"_NUN,@%MN@(NUMUC,"COM")=@GLO@(NOMATN,"UCONT",NUN,"COM")
 Q
UCONT(NOMATN,NUMUC) 
 N GLO
 S GLO="^[QUI]RQSGLO(""ATN"")"
 Q:'($D(@GLO@(NOMATN,"UCONT",NUMUC,"ROUT")))
 D @(@GLO@(NOMATN,"UCONT",NUMUC,"ROUT")_"(NOMATN)")
 Q

