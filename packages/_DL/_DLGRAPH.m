;%DLGRAPH^INT^1^59547,73868^0
JLGRAPH ;
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
CONST(JALON,GRAPH,GRAPHINV) 
 N ORIG,DEST
 K @(GRAPH),@(GRAPHINV)
 Q:'($D(@JALON))
 S ORIG=""
BCONS1 S ORIG=$O(@JALON@(ORIG))
 Q:ORIG=""
 S DEST=""
BCONS2 S DEST=$O(@JALON@(ORIG,DEST))
 G:DEST="" BCONS1
 G:@JALON@(ORIG,DEST)'>0 BCONS2
 S @GRAPH@(ORIG,DEST)=@JALON@(ORIG,DEST)
 S @GRAPHINV@(DEST,ORIG)=@JALON@(ORIG,DEST)
 G BCONS2
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
EXTREMITE(DESOP,GRAPH,GRAPHINV,FEUILLES,RACINES) 
 N SOM
 K @(FEUILLES),@(RACINES)
 S SOM=""
BEXTR S SOM=$O(@DESOP@(SOM))
 Q:SOM=""
 S:'($D(@GRAPHINV@(SOM))) @FEUILLES@(SOM)=""
 S:'($D(@GRAPH@(SOM))) @RACINES@(SOM)=""
 G BEXTR

