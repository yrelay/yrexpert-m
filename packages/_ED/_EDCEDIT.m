EDCEDIT ;
 
 
 
 
 
 
 
 
DEB Q:'($D(^PAGE($J)))
 N PAGE,LI,COL,SLI,DEBUT S DEBUT=0
 
 
 
 S %FONC="INIT.TEXTE" D ^%PBPZ
 S PAGE=""
PAGE S PAGE=$O(^PAGE($J,PAGE)) G FIN:PAGE=""
 
 S LI=""
LIGNE S SLI=LI,LI=$O(^PAGE($J,PAGE,LI))
 
 
 I LI="" U %PORT W *27,*12 G PAGE
 S COL=""
 I DEBUT F %U=1:1:LI-SLI U %PORT W *27,!
 S DEBUT=1
COLONNE S COL=$O(^PAGE($J,PAGE,LI,COL)) G LIGNE:COL="" S QQ=^PAGE($J,PAGE,LI,COL) X QQ G COLONNE
FIN 
 
 K ^PAGE($J)
 Q
