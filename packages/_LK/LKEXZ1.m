;LKEXZ1^INT^1^59547,74867^0
LKEXZ1 ;
 
GROUPC(N,V) 
 N W,V1 S W=0
 S V1=@LOCAL@("VT","VT")_V
 I $L(V1)<N Q $$PC1
 
 S V=$E(V1,1,N),FLAG=1
 S @LOCAL@("VT","VT")=$E(V1,N+1,999)
 W:W !,$E(V,1,RM)
 Q 0
 
PC1() S @LOCAL@("VT","VT")=V1
 S FLAG=0
 Q 0
 
 
42(V) 
 S V=$$42^%QMDATE1(V)
 Q 0
 ;

