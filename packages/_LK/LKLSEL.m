;LKLSEL^INT^1^59547,74867^0
LKLSEL(APP,NODE,SL) 
 
 
 
 N RECUP,SEL,TEMP,ER,RECUP1
 S TEMP=$$CONCAS^LKZ("^TEMPORAI($J)",$P($ZPOS,"^",2))
 S RECUP=$$CONCAS^LKZ("^[QUI]LK",APP_","_NODE)
 S RECUP1=$$CONCAS^LKZ(TEMP,"RECUP1")
 K @(TEMP)
 S TITRE=$$^%QZCHW($E("Sel. de recuperations "_APP_" "_NODE,1,37))
 D T
 D SELECT^LKSEL(RECUP1,SL,TITRE)
 K @(TEMP)
 Q
T N I,H S H=$O(@RECUP@("")) F I=1:1 Q:H=""  S @RECUP1@($$TEMPSNO^%QMDATE(H))=H,H=$O(@RECUP@(H))
 Q
TEST K ^TOTO
 S SEL=$$CONCAS^LKZ("^TOTO","SEL") B
 D ^LKLSEL("AVEX","A1",SEL)
 Q

