;LKNMECRI^INT^1^59547,74867^0
LKYECRI(APP,NODE,DATE,DATA,LISTE,IO) 
 
 
 
 
 
 
 
 N DAT,DAT2,LILA2,BASE2,TEMP
 N BASE,DEBO,END,FIN,FINOI,I,L,LIST,LISTE,LA,LT,LVAL,NENREG,NOP,RET
 N LILA,M,ER,MSG,LIEN,ROOT
 D ^LKNMGO(LISTE,BASE,DATA)
 D ^LKNMGONE(APP,IO)
 D TRANSF
 Q
 
TRANSF N %DIR
 I IO'=47 D INT^%DIR S IO=%DIR_IO
 D HERIT^%QAX($I,IO)
 O IO:("EFL":LONG:8000) U IO W:IO=47 *-5 D TRANS W:IO=47 *-5 C IO U 0 Q
TRANS N I,J
 U 0 D POCLEPA^%VVIDEO S DX=0,DY=23
 S J=$O(@ROOT@(1,""))
 F I=1:0 Q:J=""  U IO W @ROOT@(1,J) S J=$O(@ROOT@(1,J)) U 0 X XY W J
 Q
MES(M) D ^%VZEAVT(M) Q
 ;

