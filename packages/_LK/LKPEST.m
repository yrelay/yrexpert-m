;LKPEST^INT^1^59547,74867^0
LKPEST(TEMP,TABERR) 
 
 
 
 N I,J,ER,X,F
 D CLEPAG^%VVIDEO S F=15
 S ER=$O(@TABERR@("")) D LIB
 F J=1:1 Q:ER=""  D T S F=F+14 D CLEPAG^%VVIDEO S DX=0,DY=1 D LIB
 Q
T F I=1:1 Q:(ER="")!(I=F)  D AFF S ER=$O(@TABERR@(ER))
 D POCLEPA^%VVIDEO W $$^%QZCHW("Tapez [RETURN]") R X
 Q
AFF S DX=0,DY=DY+1 X XY W ?5,$$^%QZCHW($$ERRM^LKPHYERR(ER)),?60,$$NERR^LKPHYERR(ER) Q
LIB S (DX,DY)=0 X XY W !,?20,$$^%QZCHW("Status & Erreurs : lecture/ecriture bande")
 S DX=0,DY=2 X XY W ?7,$$^%QZCHW("Libelle"),?60,$$^%QZCHW("nombre d'occurences")
 Q

