;LKT^INT^1^59547,74867^0
LKT ;
TEST F I=1:1:5 S A=$$X W !,"A= ",A
 Q
X() S $ZT="G ER"
 D Y
 Q 0
Y W !," I ",I
 I I=2 W !,10/0
 W !,"PAS D'ERREUR"
 Q
ER W !,"ERR "
 Q 1
TEST2 N I S U="L",DATA="^TOTO($J)" K @(DATA)
 F I=1:1:3 D T(DATA,.U),T1
 Q
T(X,U) N J F J=1:1:4 S @X@(I_J)=J_I
 S U="L"_J
 Q
T1 N K,A S A=$O(^TOTO($J,"")) W !,"U ",U
 F K=1:1 Q:A=""  W !,"A ",A,?20,"VAL ",^TOTO($J,A) S A=$O(^TOTO($J,A))
 Q

