;LKTXTN^INT^1^59547,74868^0
LKTXTN(R,GLO) 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 N VECTI,ROUT,P,V,L,L1,L2,L3,L4,L5,I,J,ROOT
 S VECTI=$P(R,"^",1),ROUT=$P(R,"^",2)
 S ROOT1=GLO_"(" S:GLO["(" ROOT1=$$EX^LKZ(GLO)_","
 S L="ZL @ROUT X L1"
 S L1="F I=0:1 X @L6 Q:P=""""  X L3"
 
 S L3="X @L5 S ROOT=ROOT1_P_"")"" S @ROOT=V"
 
 S L5="""S V=$P($T(""_VECTI_""+""_I_""),""""|"""",3)"""
 S L6="""S P=$P($T(""_VECTI_""+""_I_""),""""|"""",2)"""
 
 X L Q
 
TEST K ^LK("TEMP") D ^LKTXTN("NSC^LKLATT","^TOTO($J,""AA"")")
 Q
T S L4="""S P=$P($T(""_VECTI_""+""_I_""),""""|"""",2)"""
 ;

