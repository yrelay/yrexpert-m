;LKYLIEN^INT^1^59547,74868^0
LKYLIEN(BASE,INT) 
 
 
 
 
 
 
 
 
 
 N LIENS,LIEN,I,J,T
 S LIENS=$$TEMP^%QZCHAD($P($ZPOS,"^",2),"LIENS") K @(LIENS)
 D LIEN^%QSGEST9(BASE,LIENS)
 S J=""
 F I=0:0 S J=$O(@LIENS@(J)) Q:J=""  S T=$$TYPE^%QSGEST9(BASE,J) I ((T=0)!(T=4))!(T=5) K @LIENS@(J)
 S J=$O(INT(""))
 F I=0:0 Q:J=""  K @LIENS@(J) S J=$O(INT(J))
 Q:@LIENS=0 ""
 
 Q:$O(@LIENS@(""))="" ""
 
 S LIEN=$$SEL^%QSGEST6(LIENS,$E("LIENS ISSUS DE "_$$NOMLOG^%QSF(BASE),1,40))
 K @(LIENS) Q LIEN

