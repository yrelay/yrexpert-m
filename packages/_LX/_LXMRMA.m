;%LXMRMA^INT^1^59547,73870^0
LEXMRMA ;
 
 
 
 
 N MOT,LAS,AS,LIB1,LIB2,LIB3,LSTO
 S LIB1=$$^%QZCHW("Mot reserve cree par Yrelay.")
 S LIB2=$$^%QZCHW("Ne pas modifier. Ne pas supprimer.")
 S MOT=""
BMOT S MOT=$O(^MOTRES(MOT))
 Q:MOT=""
 W !,MOT
 S $P(^MOTRES(MOT),"^")=LIB1
 S $P(^MOTRES(MOT),"^",2)=""
 G BMOT
 
UNIT S MOT=""
BMOTU S MOT=$O(^MOTRES(MOT))
 Q:MOT=""
 W !,MOT
 I $P(^[QUI]TOLEX(MOT),"^",6)'="" S $P(^MOTRES(MOT),"^",6)=$P(^[QUI]TOLEX(MOT),"^",6)
 I $P(^[QUI]TOLEX(MOT),"^",7)'="" S $P(^MOTRES(MOT),"^",7)=$P(^[QUI]TOLEX(MOT),"^",7)
 G BMOTU
