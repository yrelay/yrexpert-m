;%MZPNUME^INT^1^59547,73872^0
MOZPNUME ;
 
 
 
 
 
 D CLEPAG^%VVIDEO
 S DXG=0,DXD=39,DYH=0,DYB=4,GRAPH=1,MSG="VALEURS NUMERIQUES"
 D 4^%VZCD
L0 S DX=0,DY=10 X XY D CLEBAS^%VVIDEO
 S DX=10,DY=10 X XY W $$^%QZCHW("Longueur : ")
 G:'($D(^[QUI]MOZP("NUMERIQUE"))) L1
 S (DX,PCX)=$X,(DY,PCY)=$Y,YG=^[QUI]MOZP("NUMERIQUE")
 X XY W YG X XY D ECHOFF^%VVIDEO,BEG^%VLEC,ECHON^%VVIDEO G CTR
L1 S DX=$X,DY=$Y D ^%VLEC
CTR Q:CTRLA!(Y1="")
 I Y1'?1N.N D ^%VSQUEAK G L0
 S ^[QUI]MOZP("NUMERIQUE")=Y1 Q

