PTEK4014 ;
 
 
 
 
TEK(X,Y,YH,EX,YL,XH,XL) 
 S YH=((Y\4)\32)+32
 S EX=((((Y-((Y\4)*4))*4)+X)-((X\4)*4))+96
 S YL=((Y\4)-(((Y\4)\32)*32))+96
 S XH=((X\4)\32)+32
 S XL=((X\4)-(((X\4)\32)*32))+64
 Q
XY(XH,XL,YH,YL,X,Y) 
 S X=(((XH-32)*128)+((XL-64)*4))+128,Y=(((YH-32)*128)+((YL-96)*4))+256 Q
T1 W !,"X=" R X Q:X=""  W " Y=" R Y D TEK(X,Y,.YH,.EX,.YL,.XH,.XL) W !,YH," ",EX," ",YL," ",XH," ",XL G T1
T2 W !,"XH=" R XH Q:XH=""  W " XL=" R XL W " YH=" R YH W " YL=" R YL D XY(XH,XL,YH,YL,.X,.Y) W !,X," ",Y G T2

