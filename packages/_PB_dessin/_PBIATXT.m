;%PBIATXT^INT^1^59547,73873^0
PTIATEXT ;
 N BID,CTRLA,DX,DY,R,SENS,X,Y,Y1
 
 D EFFBAS^%PBMEN,AFF^%PBMEN(0,0,"Pointez la position du texte avec la mire")
 D ^%PBPZGRA S R=$$LOC^%PBMFN(.X,.Y)
 G:R=0 FIN
L0 
 S I=$A("H") G 65
 D ^%VSQUEAK G L0
1 G FIN
6 G FIN
65 S SENS=$C(I) G S
66 G 65
72 G 65
86 G 65
S D ^%PBPZALF,POCLEPA^%VVIDEO W "Texte : " S DX=$X,DY=$Y D ^%VLEC
 D POCLEPA^%VVIDEO,^%PBPZONG
 I CTRLA!(Y1="") G FIN
 D ^%PBPZGRA,TEXTE^%PBMFN(X,Y,Y1,0,SENS)
 S R=$$ADD^%PBSOV(O,V,$$TEXTE^%PBEL(X,Y,Y1,SENS))
FIN Q

