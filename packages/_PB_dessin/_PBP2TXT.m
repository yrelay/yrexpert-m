PT2TEXT ;
 N C,DX,DY,%FOINC,I,O,T,%9X,X,%9Y,Y S O=ENVL(%TERM,"ORIENTATION")
 S X=ENVL(%TERM,"X"),Y=ENVL(%TERM,"Y"),T=ENVL(%TERM,"TAILLE")
 I O="HORIZONTALE" S DX=50*T,DY=0
 E  S DX=0,DY=-45*T
