 F I=1:1:$L(%9STR) U %PORT W "TEXT " S C=$E(%9STR,I,I) W $S(C="""":"'"_C_"'",1:""""_C_""""),", E; ",! S %FONC="MOUVREL",%9X=DX,%9Y=DY D ^%PBPZ
FIN Q

