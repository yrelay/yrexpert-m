GRINTGRO ;
SHORT N AFF
 S DY1=$Y,DX1=41 Q:'($D(^PAD($J,PAD,DY1)))
 S OBJ=^PAD($J,PAD,DY1)
 I @CUSTOM1@("INTGR")'="" D AFF(@CUSTOM1@("INTGR"),OBJ,.AFF) D:AFF SCREESH^%QFECRAN S RETOUR=1 Q
 S %OO=OBJ_"/ ",%L=$L(%OO)
 D BLD^%VVIDEO,CUROF^%VVIDEO
 S %ATTR=-1 F %I=0:0 S %ATTR=$N(^[QUI]GRATT(NUFA,"G",%ATTR)) Q:%ATTR=-1  S %T=%OO_%ATTR_": "_$S($D(^GRV($J,OBJ,%ATTR)):^GRV($J,OBJ,%ATTR),1:"?") D POCLEPA^%VVIDEO W $E(%T,1,79) H 1
 K %I,%OO,%ATTR,%T D NORM^%VVIDEO,CURON^%VVIDEO S RETOUR=1 Q
 
 
 
 
 
 
AFF(ADR,OBJ,AFF) 
 D @ADR
 Q

