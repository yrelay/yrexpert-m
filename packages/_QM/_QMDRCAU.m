AN S REFUSE=1,CTRLF=0 I RESUL(IC)="" S TAB(IC)=$P($$DATE^%QMDATE,"/",3) D ^%VAFFICH S REFUSE=0 Q
 Q:RESUL(IC)'?2N
 S REFUSE=0
 Q
