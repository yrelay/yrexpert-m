 I RESUL(IC)="" S TAB(IC)="STANDARD" D ^%VAFFICH S REFUSE=0 Q
 I RESUL(IC)="?" D INTRG Q
 
 Q:'($D(^[QUI]PLCAREGI(RESUL(IC))))
 S REFUSE=0 Q
