;%QMDRSEJ^INT^1^59547,73877^0
QMDRSEJ ;
 ;;En entree DATE
 ;;En sortie JOUR,DSEM(debut semaine $H),DS(DATE en $H)
JDH(DATE,DSEM,DN,JOUR) 
 S %DS="" F %U=2,1,3 S %DS=%DS_$S(%DS="":"",1:"/")_$P(DATE,"/",%U)
 D INT^%DATE S JOUR=$P("Jeudi,Vendredi,Samedi,Dimanche,Lundi,Mardi,Mercredi",",",(%DN#7)+1),DSEM=%DN-(((%DN#7)+3)#7),DN=%DN
 K %DN,%DS Q
DAT(DH,DATC) 
 ;;En entree DH date($H)
 ;;En sortie DATC (JJ/MM/AA)
 S DATC=$$DATEX^%QMDATE(DH)
 Q
 S:DH'>0 DH=21915 S DD=$ZD(DH,1),DATC="" F %U=2,1,3 S DATC=DATC_$S(DATC="":"",1:"/")_$P(DD,"/",%U)
 K DD,%U Q

