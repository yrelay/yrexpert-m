;%QNEQM5^INT^1^59547,73879^0
QNEQM5(STO,K,CYCLE,STEMPS,NCLASSE,EQUI,EQUIR) 
 
 N I,CLASSE,J,IND,VALC,MAXT
 S MAXT=0
 S CLASSE=$O(@STO@(K,"CLASSE",""))
 F I=0:0 Q:CLASSE=""  D TR1 S CLASSE=$O(@STO@(K,"CLASSE",CLASSE))
 
 S EQUI=$J((STEMPS/(NCLASSE*CYCLE))*100,0,2)
 S EQUIR=$J((STEMPS/(NCLASSE*MAXT))*100,0,2)
 Q
TR1 S IND=$O(@STO@(K,"CLASSE",CLASSE,""))
 S VALC=0
 F J=0:0 Q:IND=""  D TR2 S IND=$O(@STO@(K,"CLASSE",CLASSE,IND))
 I VALC<MAXT Q
 S MAXT=VALC
 Q
TR2 S VALC=VALC+@STO@(K,"ELT",IND,"VAL")
 Q

