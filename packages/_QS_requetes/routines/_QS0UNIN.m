;%QS0UNIN^INT^1^59547,73880^0
ZLIGUNIN() ;;04:59 PM  20 Jun 1989
 
 
 
 
 N %I,I,NEWLIST,RESULTAT,MES
 D ^%QUAPAD($$^%QZCHW("UNION"))
 D ENS^%QUAPAGM("RESULTAT") G:'($D(RESULTAT)) FIN
 
 S MES=$$TSTUN^%QS0CALC("RESULTAT")
 I MES'="" D ^%VZEAVT(MES) Q 0
 D MSG^%VZEATT($$^%QZCHW("Union en cours ..."))
 S NEWLIST=$$GEN^%QCAPOP("ZUN")
 D UNION^%QS0CALC("RESULTAT",NEWLIST)
 D POCLEPA^%VVIDEO W $$^%QZCHW("Union terminee --> ") D BLD^%VVIDEO W NEWLIST D NORM^%VVIDEO
 S I="" F %I=0:0 S I=$O(RESULTAT(I)) Q:I=""  D USE^%QS0(I)
 D INIT2^%QUAPAGM,AFFP^%QUAPAGM(NEWLIST)
 S R=2
FIN D POCLEPA^%VVIDEO Q R

