;%QS5^INT^1^59547,73880^0
%QS5 ;
 
 
 
 
 
DAFF() N NEWREQ,REQ
 D ^%QUAPAD($$^%QZCHW("DUPLICATION D'UNE REQUETE"))
 S REQ=$$UN^%QUAPAGM Q:REQ="" 1
LAFF D POCLEPA^%VVIDEO S Y1=$$^%VZAME1($$^%QZCHW("Nom de la recopie : "))
 Q:CTRLA!(Y1="") 1 S NEWREQ=Y1
 I $D(^[QUI]RQS1(NEWREQ)) D POCLEPA^%VVIDEO,^%VZEAVT($$^%QZCHW("Il existe deja une requete de ce nom ... refusee ")),POCLEPA^%VVIDEO G LAFF
 D ^%QS6,CREER^%TLQUER("q."_NEWREQ)
 D INIT2^%QUAPAGM,AFFP^%QUAPAGM(NEWREQ) Q 2

