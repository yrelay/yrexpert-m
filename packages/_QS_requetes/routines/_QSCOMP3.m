;%QSCOMP3^INT^1^59547,73881^0
RQSCOMP3 ;
 
 
 
 
M(M) D ^%VZEAVT(M) Q
 
PASS 
 
 D RPASS3(A) Q
RPASS3(A) Q:A=0  N CH,I,N,RD,SA
 S RD=$$RED^%ANA(A,"REPERTOIRE")
 F I=1:1 S SA=$$RED^%ANA(A,I) Q:(SA="")!(SA=0)  D T3,RPASS3(SA)
 Q
T3 S CH=$$RED^%ANA(SA,"CHEMIN"),N=$$NEW^%ANA
 D SET^%ANA(N,"CALCUL","CHEMIN")
 D SET^%ANA(N,"CHEMIN",CH)
 D SET^%ANA(N,"REPERTOIRE",RD)
 D SET^%ANA(N,"DESTINATION",RD)
 D SET^%ANA(N,"SOURCE",$$RED^%ANA(SA,"REPERTOIRE"))
 D SET^%ANA(N,1,SA),SET^%ANA(A,I,N)
 D SET^%ANA(N,"PERE",A),SET^%ANA(SA,"PERE",N) Q

