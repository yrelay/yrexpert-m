;%QSCOMP7^INT^1^59547,73881^0
RQSCOMP7 ;
 
 
PASS N FI,FF,STOP,I,J,N,%IN,%N,NN,FG,FD,PFG,PFD,ICOUR
 S N=-1 F %N=0:0 S N=$N(SAP(N)) Q:N=-1  D TN
 Q
TN S NN=TABIND(SAP(N))
 S FG=$$RED^%ANA(N,1),PFG=$$RED^%ANA(FG,"PERE"),FD=$$RED^%ANA(N,2),PFD=$$RED^%ANA(FD,"PERE"),STOP=0
 F I=1:1 S J=$$RED^%ANA(NN,I) Q:J=0  D SUBST Q:STOP=2
 Q
SUBST Q:(J'=PFG)&(J'=PFD)
 I J=PFG D SET^%ANA(NN,I,N) S STOP=STOP+1 Q
 S STOP=STOP+1 F %IN=I:1 Q:$$RED^%ANA(NN,%IN)=0  D SET^%ANA(NN,%IN,$$RED^%ANA(NN,%IN+1))

