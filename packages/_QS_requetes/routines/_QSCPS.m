;%QSCPS^INT^1^59547,73881^0
QSCPS(STRUCT) 
 
 
 
 
 
 
 
 
 
 
 
 N BASE,REP,I,REPX,ERR
 S BASE=$O(@STRUCT@("S",""))
 Q:BASE="" 1
 S REP=$$NOMLOG^%QSF(BASE) Q:REP="" 1
 S @STRUCT@("S",BASE,"LO")=REP
 I ($D(@STRUCT@("S",BASE,"BDI"))#10)=1 S REP=$$NOMLOG^%QSF(@STRUCT@("S",BASE,"BDI")) Q:REP="" 1 S @STRUCT@("S",BASE,"BD")=REP
 S REP=$O(@STRUCT@("S",BASE,"R","")),ERR=0
 F I=0:0 Q:(REP="")!(ERR=1)  S REPX=$$NOMINT^%QSF(REP) S:REPX="" ERR=1 S:ERR=0 @STRUCT@("S",BASE,"R",REP)=REPX S REP=$O(@STRUCT@("S",BASE,"R",REP))
 Q ERR

