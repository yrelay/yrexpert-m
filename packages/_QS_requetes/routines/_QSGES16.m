;%QSGES16^INT^1^59547,73881^0
QSGES16(BASE,INT) 
 
 
 
 
 
 
 
 
 
 
 
 N LIENS,LIEN,I,J,TEMP
 S TEMP=$$TEMP^%QZCHAD($P($ZPOS,"^",2),"")
 S LIENS=$$CONCAS^%QZCHAD(TEMP,"LIENS")
 K @(TEMP)
 D LIEN^%QSGEST9(BASE,LIENS)
 S J=$O(INT(""))
 F I=0:0 Q:J=""  K @LIENS@($$NOMLOG^%QSF(J)) S J=$O(INT(J))
 I @LIENS=0 K @(TEMP) Q ""
 I $O(@LIENS@(""))="" K @(TEMP) Q ""
 S LIEN=$$SEL^%QSGEST6(LIENS,$E($$^%QZCHW("QUEL LIEN DE")_" "_$$NOMLOG^%QSF(BASE)_" ?",1,40))
 K @(TEMP)
 Q LIEN

