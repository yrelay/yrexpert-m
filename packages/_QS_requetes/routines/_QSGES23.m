;%QSGES23^INT^1^59547,73881^0
%QSGES23 ;
 
 
 
 
 
 
 
 
 
 
 
 
LIENNOM(R1,GLO) 
 N NL,L,I,CH,TYP,PL
 S @GLO=0,NL=0
 Q:$D(^[QUI]RQSLIEN(R1))=0
 S L="" F I=1:1 S L=$O(^[QUI]RQSLIEN(R1,L)) Q:L=""  S CH=^[QUI]RQSLIEN(R1,L) D TLI
 S @GLO=NL
 Q
TLI S PL=$P(L,".")
 Q:(PL="INVERSE")!(PL="LIEN")
 S TYP=$P(CH,"^",1)
 Q:((TYP'=3)&(TYP'=4))&(TYP'=5)
 S @GLO@(L)=TYP,NL=NL+1
 Q

