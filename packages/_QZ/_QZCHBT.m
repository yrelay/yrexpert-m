;%QZCHBT^INT^1^59547,73887^0
ZCHBKPT ;
 
 K BKPT S PARA=$$GETLBLAN^%VTLBLAN(PARA)
 S RS=PARA,%CAR=" " D ^%QZCHNBC S NBCAR=%NBCAR+1
LOOP F UU=1:1:NBCAR S BKPT(UU)=$P(PARA,%CAR,UU)
 S PARA="",UU=-1 F VV=0:0 S UU=$N(BKPT(UU)) Q:UU=-1  I BKPT(UU)'="" S PARA=PARA_$S($E(PARA,$L(PARA))=".":"",1:".")_BKPT(UU)
 S PARA=$E(PARA,2,299) G:%CAR="." FIN
 
FIN K %NBCAR,BKPT,UU,%CAR,VV,RS Q
 
ADR(PARA) 
 N %NBCAR,NBCAR,BKPT,UU,%CAR,VV,RS
 D ZCHBKPT
 Q PARA
TEST W !,"A tester : " R PARA Q:PARA=""  D ^%QZCHBT W !,?10,"===> """,PARA,"""" G TEST

