;%QZCHCO^INT^1^59547,73887^0
ZCOMPCH ;
 
 
 I %CH1=%CH2 S %EG=1,%SUP=0 Q
 K TABZCH S TABZCH(%CH1)="",TABZCH(%CH2)="",IZCH=$N(TABZCH(-1)),%SUP=IZCH=%CH2,%EG=0 K IZCH,TABZCH Q
TEST W !,"Chaine no 1 : " R %CH1 Q:%CH1=""
 W !,"Chaine no 2 : " R %CH2 D ZCOMPCH
 W:%EG !,"La chaine no 1 est egale a la chaine no 2"
 W:%SUP !,"La chaine no 1 est superieure a la chaine no 2"
 W:'(%SUP) !,"La chaine no 1 est inferieure a la chaine no 2"
 G TEST

