;%QZCHFO^INT^1^59547,73887^0
QZCHFO(DATA,ER) 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 N LS,I,J,ENRG,SEP
 S LS=$L($G(@DATA@("S"))),ENRG="",ER=0,SEP=$G(@DATA@("S")),CH=""
 S I=$O(@DATA@("D",""))
 F J=0:0 Q:I=""  D F S I=$O(@DATA@("D",I))
 S CH=$E(CH,1,$L(CH)-$L(SEP))
 S CH=$$FORMAT(CH,@DATA@("L"))
 Q CH
F S CH=CH_$$FORMAT($G(@DATA@("D",I,"D")),@DATA@("D",I,"T")-$L(SEP))_SEP
 Q
FORMAT(S,SI) Q $E(S,1,SI)_$J("",SI-$L(S))

