;%SYSGLRES^INT^1^59547,73892^0
%SYSGLRES ;
 
 
 
 
 D MODEL,REPAR,GLOTOT,LEX,ANAT,ANAR,TOTEM
 Q
MODEL 
 N MOD
 W !,"nom du modele" R MOD
 D ^%QSMODEL(1,MOD,QUI,WHOIS)
 
REPAR G QUERY1^%QSREPAR
 
 
GLOTOT D ADR^%QCAGLC("^[QUI]EXPLICITE","^[QUI]EXPLICI")
 K ^[QUI]EXPLICITE
 D ADR^%QCAGLC("^[QUI]IMPLICITE","^[QUI]IMPLICI")
 K ^[QUI]IMPLICITE
 Q
 
 
LEX D ^TOV2 Q
 
 
ANAT N VU,TRT,%Y,RES,I,CTRLA,ABANDON,SOR,IMP,ANALGLOB
 S ANALGLOB=1
 G 0^%TLALLAN
 
 
ANAR N VU,REQ,%Y,RES,I,CTRLA,ABAN,SOR,IMP
 G 0^%QSALLAN
 
 
TOTEM D ^TOV3,^TOV4,^TOV5,^TOV6,^TOV51,^TOV52
 Q

