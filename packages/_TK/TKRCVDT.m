;TKRCVDT^INT^1^59547,74869^0
TKRCVDT ;
CTL d INIT
 d HS
 d RCV
 d DONE
 q
 
INIT s crt=$i,port=100,file=10,eol=$c(10),eom=$c(12)
 o port:(speed=9600:parity="NONE":charbits=8:stopbits=1:xon=1)
 o file:(mode="W":file="TKRCV.PRG")
 u port:(em=0)
 u file w "header 1",eol,"header 2"
 q
 
HS u port r x u crt w x u port w 1,*13 q
 
RCV f i=1:1 d READ q:x["***END***"
 u file w eol,eom
 q
 
READ u port r x q:x["***END***"
 s l=+($e(x,1,3)),x=$e(x,4,999) i $l(x)'=l u crt w !,!,*7,"BAD XMT",!,x,!,l,"'=",$l(x) r z u port w "X",*13 q
 u crt w !,x u file w:x'[" " eol w x,eol u port w "R",*13 q
 
DONE u crt w !,!,x q
 ;

