;%TLCACOT^INT^1^59547,74028^0
ARCOTGTB ;
 
 N %PCH
 S %PCH=%VAL,%LLIM=5729582717928403E-12 I '($$PARA^%QZNBN1(%VAL)) S %ACTG="1??" G FIN
 I %PCH>%LLIM S %ACTG=0 G FIN
 I %PCH<(-(%LLIM)) S %ACTG=180 G FIN
 I $L($P(%PCH,".",2))>18 S %PCH=$P(%PCH,".",1)_"."_$E($P(%PCH,".",2),1,18)
 S %PCH=%PCH*1 I $D(^ARCCTG(%PCH)) S %ACTG=^ARCCTG(%PCH) G FIN
 S V=%PCH,V2=$O(^ARCCTG(V)),V1=^PRECSTCT("ARCCTG",V2)
CALASN S %ACTG=((^ARCCTG(V2)-^ARCCTG(V1))*(V-V1))/(V2-V1),%ACTG=%ACTG+^ARCCTG(V1)
FIN Q
TEST W !,"Arc cotangeante (" R %VAL Q:%VAL=""  D ARCOTGTB W ")=",%ACTG G TEST

