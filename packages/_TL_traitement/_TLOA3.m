;%TLOA3^INT^1^59547,74030^0
%TLOA3 ;
 
 
 
 
 
 
 
 
 
 
 
 
PARTIT(IDEB,NBREG) 
 N L,BASE,REP,AT,TEMP,GLR,NAR,ATTA,INDA,VARA,REPA,IVAL,VAL
 S GLR=$$TEMP^%SGUTIL
 
 S L=V(IDEB+6)
 S:L=$C(0) L=PARCO("LISTE")
 S BASE=$$^%QSCALVA("L0",L,"OBJET")
 S REP=$$NOMINT^%QSF(BASE)
 
 S AT=V(IDEB+5)
 D PART^%QS0PART(REP,L,AT,GLR)
 
 
 S ATTA=V(IDEB+1),REPA=V(IDEB+3),VARA=V(IDEB+4)
 
 
 
 S IVAL="" F I=1:1 S IVAL=$O(@GLR@(IVAL)) Q:IVAL=""  S VAL=@GLR@(IVAL) D PA^%QSGESTI(REPA,VARA,ATTA,VAL,IVAL)
FIN K @(GLR) Q 1

