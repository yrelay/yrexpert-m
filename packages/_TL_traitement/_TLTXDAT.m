;%TLTXDAT^INT^1^59547,74031^0
TLTXDAT(ATR) 
 
 
 
 
 
 
 
 N CPT,INDIV,NOML,TEMP
 S TEMP=$$TEMP^%QZCHAD($P($ZPOS,"^",2),"") K @(TEMP)
 I $$TYPE^%QSGESPE(REPL_"/"_ATR)'="DATE" D ^%VZEAVT($$^%QZCHW("Cet attribut n'est pas de type DATE")) Q
 D ^%VZEATT,^%QCAMEMS
 
 D ^%QMDATR(REPL,ATR,.TEMP)
 G:'($D(@TEMP)) FIN
 S NOML=$$NOM^%QSGES11("TMP"),CPT=$$CARD^%QSGES11(TEMP)
 D COPY^%QSGES11(NOML,REP,CPT,TEMP),CREER^%QSGES11(REP,NOML,"TMP",CPT,"TEMPORAIRE","","TEMPORAIRE")
FIN D ^%QCAMEMR,POCLEPA^%VVIDEO I MODAF=1 D REAF^%TLIFEN
 K @(TEMP) Q

