CTRHEURE ;
 S %OK=1 G @$S($F(PCH,"H",0):"H",1:"NH")
H 
 S XHEUR=$P(PCH,"H",1),XMIN=$P(PCH,"H",2) I (XHEUR'?.N)!(XMIN'?.N) S %OK=0 G FIN
 S NBHP=XMIN\60,XMIN=XMIN-(NBHP*60),XHEUR=XHEUR+NBHP G FIN
NH 
 I PCH'?.N S %OK=0 G FIN
 S XHEUR=PCH\60,XMIN=PCH-(XHEUR*60) G FIN
FIN S:$L(XMIN)=1 XMIN="0"_XMIN S %RES=XHEUR_"H"_XMIN,%RESMIN=(XHEUR*60)+XMIN K NBHP,XHEUR,XMIN Q
