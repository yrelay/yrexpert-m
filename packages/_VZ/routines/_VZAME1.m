%ZMESDAT(MES) 

 N DF
 S DF=""
 
DAT W MES
 
 
 S DX=$X+1,DY=$Y
 D ADR2^%VLEC(DF)
 Q Y1
 
DATA(MES,DF) 
 G DAT

