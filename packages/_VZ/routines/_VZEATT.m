;%VZEATT^INT^1^59547,74037^0
ZATTENTE ;
ON 
 D POCLEPA^%VVIDEO S DX=0 X XY U 0 W $$BLK^%VVIDEO1_$$BLD^%VVIDEO1_$$^%QZCHW("Un instant...")_$$NORM^%VVIDEO1
 Q
OFF D POCLEPA^%VVIDEO Q
MSG(MSG) D POCLEPA^%VVIDEO S DX=0 X XY U 0 W $$BLK^%VVIDEO1_$$BLD^%VVIDEO1_$E(MSG,1,RM-5)_$$NORM^%VVIDEO1
 Q
TEST D ON H 10 D OFF Q
 
PAD D PAD1(PADMESSA) Q
 
PAD1(MSG) 
 N DX,DY
 S DX=0,DY=23
 X XY W MSG H 1
 Q

