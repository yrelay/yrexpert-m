PARTIE ;
 
RETERR S $ZT=^TOZE($I,"A") Q:%ABEND=1  S %9CH=$P(EXPFONC,"/",2),%9PART=$P(EXPFONC,"/",3),%9SEP1=$P(EXPFONC,"/",4),%9SEP2=$P(EXPFONC,"/",5)
 S @("%9CH="_%9CH)
 I %9PART'="" S PCH=%9PART D TO^%QZNBN1 S:'(ISNUM) @("%9PART="_%9PART)
 I %9SEP1'="" S PCH=%9SEP1 D TO^%QZNBN1 S:'(ISNUM) @("%9SEP1="_%9SEP1)
 I %9SEP2'="" S PCH=%9SEP2 D TO^%QZNBN1 S:'(ISNUM) @("%9SEP2="_%9SEP2)
 S %RESULTAT=$S(%9SEP1="":$P(%9CH,%9PART),%9SEP2="":$P(%9CH,%9PART,%9SEP1),1:$P(%9CH,%9PART,%9SEP1,%9SEP2))
 
 I %RESULTAT="" S %RESULTAT=" "
FIN K %9PART,%9SEP1,%9SEP2,%9CH
 Q

